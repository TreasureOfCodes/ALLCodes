/* Testing the boolean variables in if and else statements */

public class Helo_world{

    public static void main(String args[]){
		boolean isTrue = true; // variable always true
		boolean isFalse = false; // variable always false
		
		// Statement to test isTrue
		if(isTrue)
		{
			System.out.println("Hello I am true");
		}
		else
		{
				System.out.println("Hello I am false");
		}
		
		// Statement to test isFalse
		if(isFalse)
		{
			System.out.println("Hello I am true");
		}
		else
		{
			System.out.println("Hello I am false");
		}
        
    }
}