/* This code is to print from 1 to 10 using loop.*/

#include<stdio.h>

int main()
{
    int i; //to increase the value of loop

    //if we start the loop from 0 and i less than 10.
    //if will print from 0 and will end at 9.
    for(i=0; i<10; i++)
    {
        printf("%d ", i);
    }

    //if we start the loop from 1 and i less than equal to 10.
    //if will print from 1 and will end at 10.
    printf("\n");
    for(i=1; i<=10; i++)
    {
        printf("%d ", i);
    }
}
